function isHLCEnabled(npc)
	return isElement(npc) and getElementData(npc,"crp_npc") or false
end

function getNPCWalkSpeed(npc)
	if not isHLCEnabled(npc) then
		outputDebugString("Invalid ped argument",2)
		return false
	end
	return getElementData(npc,"crp_npc:walk_speed")
end

